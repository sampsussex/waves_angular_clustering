# 3. Example usage
# =====================================================================
if __name__ == "__main__":
    model = SchechterNzModel()

    # sanity-check plot of the original cumulative flux-limited predictions
    z_grid = np.linspace(0.001, 0.5, 200)
    model.plot_flux_limited(
        z_grid,
        samples=[(21.1, 1200.0, "WAVES-Wide"), (21.25, 65.0, "WAVES-Deep")],
        filename="predicted_Nz.png",
    )

    # joint 3-parameter fit of z_c(m) to the model's own predicted
    # dN/dz SHAPE, sliced into apparent-magnitude bins. No counts, no
    # survey area/size, no Poisson weighting anywhere -- every z point
    # on every slice's normalized curve is weighted equally.
    #
    # z_grid here extends to z=1.0, past the LF fit's calibrated range
    # (zfit_max=0.5) -- M*(z) and phi*(z) are being extrapolated for
    # z>0.5. from_model() prints a note when this happens.
    fitter = ZcJointFitter.from_model(
        model,
        mag_edges=np.arange(16, 23, 1),
        z_grid=np.linspace(1e-4, 1.0, 500),
    )
    fitter.fit()
    fitter.summary()
    fitter.plot(filename="zc_joint_fit_from_model.png")


